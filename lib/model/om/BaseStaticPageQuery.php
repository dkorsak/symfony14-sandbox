            if (isset($createdAt['max'])) {
                $this->addUsingAlias(StaticPagePeer::CREATED_AT, $createdAt['max'], Criteria::LESS_EQUAL);
                $useMinMax = true;
            }
            if ($useMinMax) {
                return $this;
            }
